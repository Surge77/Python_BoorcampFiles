"""
Write a program to count the number of characters present in the string.

"""

# There are multiple methods to do this

# using len() function
s = "Hello, World!"
num_chars = len(s)
print(num_chars)  # Output: 13



# using count() method
# this is mostly used to count the specific letter in a string
s = "Hello,World!"
num_chars = s.count("o")
print(num_chars)  # Output: 13

